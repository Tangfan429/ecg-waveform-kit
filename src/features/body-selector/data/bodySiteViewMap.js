export const resolveBodySiteViews = (siteCode) => {
  const normalizedCode = String(siteCode || "").trim();

  if (!normalizedCode) {
    return [];
  }

  return BODY_VIEW_ORDER.filter((viewKey) =>
    BODY_VIEW_SITE_CODES[viewKey]?.includes(normalizedCode),
  );
};

export const resolvePreferredBodyView = (siteCode, currentView = "front") => {
  const availableViews = resolveBodySiteViews(siteCode);

  if (!availableViews.length) {
    return currentView;
  }

  if (availableViews.includes(currentView)) {
    return currentView;
  }

  return availableViews[0];
};

